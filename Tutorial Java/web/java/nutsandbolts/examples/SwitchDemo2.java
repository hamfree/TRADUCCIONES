class SwitchDemo2 {

    public static void main(String[] args) {

        int mes = 2;
        int a単o = 2000;
        int numDias = 0;

        switch (mes) {
            case 1:
            case 3:
            case 5:
            case 7:
            case 8:
            case 10:
            case 12:
                numDias = 31;
                break;
            case 4:
            case 6:
            case 9:
            case 11:
                numDias = 30;
                break;
            case 2:
                if (((a単o % 4 == 0)
                        && !(a単o % 100 == 0))
                        || (a単o % 400 == 0)) {
                    numDias = 29;
                } else {
                    numDias = 28;
                }
                break;
            default:
                System.out.println("Mes no v\u00E1lido.");
                break;
        }
        System.out.println("N\u00FAmero de D\u00EDas = "
                + numDias);
    }
}
